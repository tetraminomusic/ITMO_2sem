-- 3. Вывести число фамилий без учета повторений.
-- При составлении запроса нельзя использовать DISTINCT.

SELECT
    COUNT("ФАМИЛИЯ")
AS "Количество уникальных фамилий"
FROM
    (SELECT
         "ФАМИЛИЯ"
     FROM
         "Н_ЛЮДИ"
     GROUP BY
         "ФАМИЛИЯ");

